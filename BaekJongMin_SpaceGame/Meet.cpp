// 해파리 외계인 만남
void Meet::MeetJellyFish()
{
	printf("\n");
	printf("   / ^ \\\n");
	printf("  ( o_o)                 .---.\n");
	printf("  <)   )┘               ( o_o )\n");
	printf("   //\\\\                  /|||\\\n");
	printf("  []  []                 / | \\\n\n");
	printf(" [플레이어]          [해파리 외계인]\n\n");
}

// 외계 로봇 만남
void Meet::MeetRobot()
{
	printf("\n");
	printf("   / ^\\\n");
	printf("  ( o_o)                 [o_o]\n");
	printf("  <)   )┘               /|===|\\\n");
	printf("   //\\\\                  || || \n");
	printf("  []  []                 [] [] \n\n");
	printf(" [플레이어]           [외계 로봇]\n\n");
}

